#include "libft.h"

void	ft_putsizet(size_t n)
{
	char	str[16];
	char	tab[16];
	int		i;

	ft_memcpy(tab, "0123456789ABCDEF", 16);
	i = 15;
	str[i] = '\0';
	str[i - 1] = '0';
	while (n)
	{
		str[--i] = tab[n % 16];
		n /= 16;
	}
	ft_putstr(str + i);
}
